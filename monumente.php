<h2>Coloana Ciumei </h2>
Coloana Ciumei (în germană Pestsäule, Dreifaltigkeitssäule),
 cunoscută și ca Statuia Ciumei sau Monumentul Sfintei Treimi, este înălțată în mijlocul
 Pieței Unirii din Timișoara, în concordanță cu stilul arhitectonic al clădirilor dimprejur (baroc).
 Monumentul se înscrie în tipologia coloanelor ciumei, răspândite în epoca barocului în întregul spațiu sud-german, boem și maghiar.
 Statuia amintește de încetarea epidemiei de ciumă care devastase întreg Banatul, între 1731 și 1738. În timpul epidemiei, numai la
 Timișoara și-au pierdut viața peste 1.300 de bolnavi de ciumă. Un monument similar se găsește în comuna Teremia Mare din județul Timiș.
 În anii 1970, cu ocazia construcției magazinului universal Bega, monumentul a fost mutat în Piața Unirii. Pe locul în care se afla inițial 
 se găsește în prezent Ceasul Floral.
