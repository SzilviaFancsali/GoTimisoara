<h2>Monumentul Sfintei Maria şi a Sfântului Nepomuk </h2>
Monumentul a fost realizat în stil baroc în anul 1756 la Viena, de către F. Blim şi 
E. Wasserburger şi montat în faţa fostei primării din Piaţa Paradei, azi Piaţa Libertăţii. 
În anul 1852 a fost mutat în apropierea uneia dintre porţile cazărmii Transilvania, lăsând loc amplasării unui alt monument,
Monumentul Victoriei. În anul 1964 s-a demolat cazarma pentru a face loc construcţiei hotelului Continental, iar  în anul 1974 
monumentul revine în faţa fostei primării, acolo unde îi fusese locul iniţial și unde se află şi acum. Pentru că a fost avariat
serios în timpul Revoluţiei din Decembrie 1989, ansamblul statuar ciobit de gloanţe a fost restaurat în 1992-1993.
