<h2>Statuia Lupoaicei</h2>
Statuia Lupoaicei din Timișoara este o statuie înfățișînd pe legendarii întemeietori ai Romei, frații Romulus și Remus,
 alăptați de o lupoaică. Statuia redă legenda întemeierii Romei, conform căreia cei doi frați care au întemeiat orașul 
 au fost îngrijiți de o lupoaică.
 Monumentul, aflat în Piața Victoriei, reprezintă un cadou făcut Timișoarei de către municipalitatea orașului Roma, ca un simbol al latinității care unește 
 popoarele român și italian. Este amplasată în centrul orașului, în piața dintre Operă și Catedrala Mitropolitană.
 Statuia este o copie a celebrei „Lupa Capitolina” din capitala Italiei, amplasată pe o coloană de 4,96 m
