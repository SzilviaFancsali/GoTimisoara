<h2>Monumentul Fecioara Maria </h2>
În anul 1514 la Timișoara a fost executat Gheorghe Doja, dar nu se știe unde anume a fost locul executării.
 Conform tradiției, în timp ce Doja a fost torturat și supus chinurilor, 
călugării iezuiți și iobagii cântau cântece religioase și ar fi văzut în văzduh figura Fecioarei Maria.
În acel loc a fost pusă inițial icoana Fecioarei Maria pe un stâlp, care a rămas acolo și în timpul ocupației turcești. 
După mai multe variante de monument, capela actuală a fost construită în 1906 după proiectul lui Székely László. 
Sub un baldachin neoroman susținut de șase stâlpi din granit se află statuia Sfintei Maria cioplită din marmură de Carara.
