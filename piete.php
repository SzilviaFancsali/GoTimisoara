<h2>Piața Sfântul Gheorghe </h2>
na dintre cele mai vechi pieţe ale oraşului, denumită după biserica ce purta hramul Sfântul Gheorghe, transformată în moschee pe timpul ocupaţiei otomane, a fost văduvită chiar de elementul principal în urmă transformărilor de modernizare ale oraşului. Seminarul catolic a găzduit prima instituţie de învăţământ superior laic din oraş, între anii 1845 şi 1848.

În anul 1323 este atestată în Cetatea Timişoarei biserica cu hramul Sfântul Gheorghe. Legenda spunea că biserica a fost transformată în moschee după 1552, când Timişoara este cucerită de otomani. În decembrie 2013 însă, şantierul arheologic amenajat cu ocazia reabilitării centrului istoric al Timişoarei au demonstrat că în această locaţie nu a existat biserica atestată în 1323 , cele mai vechi ruine aparţinând unei clădiri impunătoare, lungă de peste 15 m (clădirea se continuă şi în afara suprafeţei cercetate), a cărei nave se închidea cu o absidă. Prin urmare informaţia transformării bisericii în moschee nu este confirmată de cercetări, şi nici situarea bisericii amintită în documentele din secolul al XIV-lea în acest loc nu se confirmă. Este însă cert că o parte a materialului de construcţie a fost adus din demolarea unui alt edificiu construit în stil gotic, care poate fi unul ecleziastic, dar şi laic. 
