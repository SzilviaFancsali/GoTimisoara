<h2>Piața Victoriei </h2>
Piața Victoriei, fostă Piața Operei, este una din piețele centrale ale municipiului Timișoara, locul în care Timișoara a fost proclamată în 20 decembrie 1989 primul oraș liber din România. La polii opuși ai pieței se găsesc la nord Opera, iar la sud Catedrala Mitropolitană. Dinspre Operă spre Catedrală promenada de pe dreapta se numește "Corso", iar cea de pe stânga "Surogat". Ambele au ansambluri arhitecturale de importanță istorică.

În jurul pieței se găsesc importante instituții cum ar fi: Opera Română, Teatrul Național, Teatrul Maghiar de Stat, Teatrul German de Stat, Catedrala Mitropolitană Ortodoxă, Cinematografele Timiș, Capitol și Studio, Muzeul Banatului, Galeriile de artă precum și multe magazine.
