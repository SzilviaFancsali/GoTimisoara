<h2>Piața Traian</h2>
Piața Traian denumită și Piața Plângerii este înconjurată de clădiri istorice și se află în centrul cartierului istoric Fabric din Timișoara. În trecut s-a numit Kossuth tér și Hauptplatz. A fost proiectată de inginerii militari în 1740, în centrul Fabricului Vechi și este o replica mai mică a Pieții Unirii. Ea găzduia piața zilnică și târgurile săptămânale.

Se află la intersecția străzilor: Piața Traian, Strada Dacilor, Zăvoi, Costache Negruzzi, Ion Mihalache, Piața Romanilor.

A fost și este folosită de edili și cetățeni, ca spațiu civic pentru aniversări omagiale ca Ziua cartierului Fabrik, Ziua Europei, Ziua Națională a României. Se organizează concerte de muzică populară, ușoară, cultă și de promenadă, astfel realizându-se un dialog multicultural, multietnic specific orașului.
