	<p>Timișoara este situată în sud-estul Câmpiei Panonice (respectiv în sudul Câmpiei de Vest), în zona de divagare a râurilor Timiș și Bega. 
Timișoara, in traducere: „cetatea de pe Timiș” este reședința și cel mai mare oraș al județului Timiș din regiunea istorică Banat, vestul României. 
Numele localității vine de la râul Timiș (trecând la sud de municipiu), numit de romani în antichitate Tibisis sau Tibiscus. </p>

<p>Prima atestare documentară a localității Timișoara este destul de controversată, aceasta fiind plasată de specialiști în 1212 sau în 1266. În 1175 este menționat 
comitatul Timiș, dar sursele nu menționează care este centrul economic și administrativ al acestuia. În momentul atestării sale acesta făcea parte din comitatul Timiș,
 o unitate administrativ teritorială a regatului ungar. Teritoriul cunoscut mai târziu ca Banat cu centrul administrativ în „Urbis Morisena” (apoi Cenad), 
 fusese cucerit de către maghiari în jurul anului 1030 și încorporat în regatul ungar. </p>

<p>Fiind așezată într-un punct strategic, de unde putea fi controlată o mare parte a Câmpiei Banatului, atât Timișoara cât și funcția 
de comite de Timiș au devenit din ce în ce mai importante. Timișoara a primit un impuls deosebit în timpul domniei regelui Carol Robert de Anjou,
 care în urma vizitei sale din 1307 a ordonat construirea aici a unui palat regal. În timpul anarhiei feudale, acesta va muta capitala Ungariei la Timișoara. 
 Numirea lui Iancu de Hunedoara în funcția de comite de Timiș, în 1440 marchează un capitol aparte din istoria Timișoarei. 
 Iancu de Hunedoara a fost cunoscut în întreaga regiune pentru reputata victorie de la Belgrad asupra otomanilor,
 fiind considerat în acea vreme apărător al creștinătății. El a transformat orașul într-o tabără militară permanentă și în domiciliul său, 
 după ce s-a mutat aici împreună cu familia. </p>
<p>Astfel, cetatea va rămâne în posesia Corvineștilor până în 1490. 
Pe 20 decembrie 1989, Timișoara a fost declarată primul oraș din România liber de comunism. </p>

<p>Timișoara este un oraș multicultural, influențat de diversele comunități etnice, în special de cea germană, maghiară și sârbă, dar și de cele bulgară,
 italiană și greacă. Moștenirea culturală și diversitatea ofertei culturale sunt punctele forte ale orașului.
 Cu cele peste 12 instituții culturale profesioniste, Timișoara este, după București și Iași, al treilea pol cultural
 din țară din punct de vedere al mărimii și diversității ofertei culturale.[10] O mare tradiție o are teatrul timișorean,
 care prin cele trei teatre de stat, (în premieră în Europa): Teatrul Național, / Teatrul German și Teatrul Maghiar, oferă 
 spectacole în limbile română, germană și maghiară. Cele trei instituții împart aceeași clădire-simbol cu Opera Română. </p>

<p>Din punct de vedere turistic Timișoara deține cel mai amplu ansamblu de clădiri istorice din România, 
constituit din ansamblurile urbane ale cartierelor Cetate, Iosefin și Fabric. Arhitectura variată, influențele 
barocului Vienez și multitudinea de parcuri au adus Timișoarei, așa cum s-a spus mai sus, renumele de „Mica Vienă” și de „Orașul parcurilor”.</p>
