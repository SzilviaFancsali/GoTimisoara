	<h2>	Fabrica de bere </h2>
	Fabrica Timişoreana se găseşte în vechiul cartier timişorean Fabric, în apropierede Piaţa Traian, punctul de interes central al acestui cartier. Deşi clădirile fabricii au fost renovate, ele păstrează stilul arhitectural al secolului al XVIII-lea.
Alături de fabrică se găseşte şi o berărie cu terasă, care are un aer de han vechi, unde vizitatorii pot degusta beri produse local. Fabrica a fost înfiinţată în anul 1718, an de graţie în istoria Timişoarei, când oraşul a fost eliberat de sub stăpânirea otomană 
de către armatele austriece conduse de prinţul Eugeniu de Savoya. După cucerirea cetăţii, acesta dispune construirea unei fabrici 
de bere, din motive foarte practice: carenţa de apă potabilă, în oraş existând puţine fântâni, dar şi faptul că soldaţii austrieci
 fiind obişnuiţi cu berea aveau dificultăţi în a consuma apa din câmpia mlăştinoasă a Timişoarei.
