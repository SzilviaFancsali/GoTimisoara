	<h2>Hidrocentrala</h2>
	Hidrocentrala de pe Bega construită după planurile arhitectului Székely László a fost terminată în 1910. Clădirea este alcătuită dintr-un corp supraînălțat, asemănător unui turn, un corp pe malul stâng al Begăi și un corp ce se întinde deasupra apei. Turnul are un șir de arcade sprijinite pe coloane, 
