	<h2>Turnul de apă </h2>
	Turnul de apă din Iosefin este un monument industrial din Timișoara, pe strada Gheorghe Barițiu nr. 3.
	El făcea parte din sistemul de alimentare cu apă al Timișoarei de la începutul secolului al XX-lea.
	Turnul de apă din Iosefin a fost ridicat între anii 1913–1914, ca frate geamăn al celui din cartierul Fabric. 
	Turnurile, cu înălțimea de 52 m, au fost ridicate la capetele rețelei de distribuție a apei și au servit pentru
	compensarea consumului maxim din cursul zilei. Turnurile au fost realizate după un proiect al inginerilor János Lenarduzzi (1865–1916[5]) și Richárd Sabathiel (n. 28 ianuarie 1875, Budapesta – d. 14 iunie 1942, Budapesta).
