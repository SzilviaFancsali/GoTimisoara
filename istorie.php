	<div class="content_text">
	Timișoara are o istorie îndelungată care începe cu antice așezări umane pe actuala vatră 
	a orașului și mai apoi cu apariția primei cetăți fortificate, în jurul secolului XII. 
	Cetatea a trecut prin diferite stăpâniri care i-au marcat profund cursul dezvoltării. 
	A început să se dezvolte sub stăpânirea ungară a lui Carol Robert de Anjou, în secolul XIV, 
	devenind pentru scurt timp și capitală a Regatului Ungar. Cetatea a fost apoi 
	cucerită de otomani, în 1552, și pentru aproape două secole a devenit un important 
	bastion militar turcesc. În 1716 istoria Timișoarei ia o puternică întorsătură, când 
	cetatea e cucerită de Imperiul Habsburgic și începe reconstrucția și dezvoltarea orașului 
	drept capitală a Banatului. Colonizarea Banatului face din Timișoara un adevărat centru 
	multicultural și multiconfesional. Construcția canalului navigabil Bega dă un avânt puternic 
	dezvoltării economice și a comerțului. Orașul cunoaște o perioadă de înflorire fără precedent, 
	ajungând să primească numele de "Mica Vienă" ca recunoaștere a importanței și frumuseții lui. 
	În 1919, Banatul se unește cu România și în Timișoara se instaurează pentru prima dată 
	administrația română. Ocupația sovietică postbelică marchează începutul perioadei comuniste,
	perioadă care ia sfârșit în 1989 odată cu Revoluția anticomunistă: revoluție pornită tocmai
	din orașul de pe Bega.</p>
