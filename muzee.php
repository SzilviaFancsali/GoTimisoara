<h2>Muzeul Satului Bănățean </h2>
Muzeul Satului Bănățean din Timișoara este singurul muzeu cu profil etnografic din România care cuprinde centrul civic al satului, 
format din Primărie, Biserică, Școală, Casă Națională (cu destinație culturală) și birt, în care se desfășoară majoritatea activităților
 cultural-educative și științifice ale unei localități. În urma unei inițiative lansate de Asociația foștilor deportați în Bărăgan
 a fost construită în incinta Muzeului Satului Bănățean o replică fidelă a unei case de pământ bătut, acoperită cu paie, așa cum au 
 fost obligați deportații să-și construiască în plin câmp. Casa este compusă din 2 încăperi – cameră de locuit și bucătărie - mobilate
 cu puține lucruri, asemeni cu acelea pe care oamenii dislocați peste noapte au reușit să le ia cu ei. Alături de Casa Națională,
 s-a amenajat o scenă în aer liber unde, în perioada estivală, se desfășoară în fiecare duminică “hora satului” precedată de 
 spectacole etno-folclorice.
