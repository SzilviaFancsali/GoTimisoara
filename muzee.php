<h2>Muzeul Militar </h2>
Cazinoul Militar este una dintre cele mai importante construcții militare ale orașului secolul XVIII, a fost terminat în anul 1775. 
În decursul anilor clădirea a fost supraetajată și completată cu o terasă mare. Sala mare de festivități a jucat un rol important
 în viața socială a tinerilor, fiindcă aici se desfășurau celebrele baluri ale ofițerilor. În 1924 aici a fost organizată Expoziția 
 de trandafiri la care a fost prezentat pentru prima dată trandafirul negru și albastru. Din 1996 găzduiește Muzeul și Cercul Militar.
