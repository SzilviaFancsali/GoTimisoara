<h2>Muzeul de Artă </h2>
În anul 1879 a fost înregistrată prima pictură în evidențele muzeului Banatului,
 la care se mai adaugă între anii 1888-1895 donația lui Ormos Zsimond, colecționar 
 și istoric de artă, unul dintre fondatorii muzeului. Pe parcurs patrimoniul de pictură
 s-a mai completat prin noi donații și achiziții. Astfel în 1943 muzeul s-a mutat într-o aripă a Palatului Culturii,
 iar în anul 1947 în Castelul Huniade.
 În anul 1987, secția de artă a Muzeului Banatului se mută în aripa de vest a Palatului Baroc din Piața Unirii.
 Acest spațiu, însă era prea mic pentru expunerea colecțiilor. 
 Din 1 ianuarie 2006 secția devine Muzeul de Artă și care în prezent numără peste 8500 de exponate.
