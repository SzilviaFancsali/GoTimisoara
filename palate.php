<div class="text2"><h2>Palatul Apelor </h2>
Palatul Apelor, cunoscut și ca Palatul Societății de Hidroameliorări Timiș–Bega, Palatul Regionalei CFR Timișoara, Palatul Direcției Apelor Banat, este o clădire monumentală, situată la intrarea dinspre centrul orașului spre cartierul Iosefin din Timișoara. Face parte din Situl urban „Vechiul Cartier Iosefin”. Construit în 1901 pentru Societatea de Hidroameliorări Timiș Bega, între 1948–2007 a mai adăpostit Regionala CFR Timișoara. Actual aparține din nou Administrației Bazinale de Apă Banat.

Inițial palatul a fost proiectat și executat cu un singur etaj, dar peste 10 ani a fost supraetajat. În 2018 s-a încheiat o reabilitare începută în 2010 prin care s-a refăcut acoperișul, s-a hidroizolat fundația, s-au refăcut instalațiile interioare, s-a construit o nouă casă a scării, s-a amenajat mansarda și s-au refăcut finisajele, atât cele interioare, cât și bogatele decorații exterioare, caracteristice stilului Seccesion. Actual este una dintre cele mai frumoase clădiri din Timișoara.</div>
