<div class="text2"><h2>Palatul Dauerbach</h2>
Palatul Dauerbach din Piața Victoriei, a fost construit între anii 1912-1913 după planurile arhitectului László Székely. Clădirea este cunoscută sub numele de Palace, după cafeneaua și restaurantul Palace, carea au fost amenajate chiar la terminarea clădirii. În partea nordică a parterului a funcționat și o farmacie. Fațada clădirii este împărțită în cinci părți, una centrală alcătuită din trei părți, mai înaltă, care la nivelul acoperișului are trei frontoane de forma unei acolade ogivale. Cele două părți laterale au câte doi pilaștrii, iar la nivelul superior au un fronton, și un acoperiș mai scund.
</div>
</div>
<div class="mySlides1">
