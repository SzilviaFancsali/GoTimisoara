<div class="text2"><h2>Palatul Weiss</h2>
Palatul Weiss a fost construit in anul 1912 chiar la inceputul Bulevardului Republicii, pentru familia Weiss. A fost
ridicat de firma Arnold Merbl in stil eclectic, iar frontonul a fost reproiectat de Laszlo Szekely, fost arhitect- sef al Timisoarei, cu vedere spre Piata Victoriei, Hotel Timisoara si teatru.

Palatul are 27 de apartamente, unele chiar si cu sase camere, in care locuiesc familii de timisoreni, iar la parter functioneaza magazine.
