<div class="text2"><h2>Palatul Neuhausz</h2>
Palatul Neuhausz este, de asemenea, o cladire simetrica. A fost construit in perioada 1911-1912 intr-o combinatie de
stiluri eclectic, seccesion si art nouveau maghiar. Astazi, la peste 100 de ani de la construirea palatului, doar cativa timisoreni mai stau in cele 22 de apartamente. Restul sunt chiriasi ai unor proprietari plecati in strainatate in perioada comunista si care in ultimii ani si-au castigat in instanta dreptul de proprietate.</div>
