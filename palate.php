<div class="text2"><h2>Palatul Brück</h2>
Casa sau Palatul Brück este o clădire din Piața Unirii din Timișoara construită în 1910 de către arhitectul Székely László în stilurile Art Nouveau și Secession, cu un caracter ușor eclectic.
Este o clădire cu patru nivele la parterul ei fiind și o farmacie. Clădirea construită de familia Brück a fost frumos restaurată în anul 2012.
