<div class="text2"><h2>Casa Mercy</h2>
Cunoscut si drept Palatul Mercy, Casa contelui Mercy, aflat pe strada Proclamatia de la Timisoara, a fost resedinta lui Florimund de Mercy d’Argenteau, fost Guvernator al Banatului.

Palatul Mercy fost construit in secolul al XVIII-lea in stilul neoclasic, fiind una dintre cele mai reprezentative cladiri ale acestui stil din Timisoara. Chiar daca nu se cunosc multe detaii din istoria imobilului, se stie ca aspectul actual se datoreaza unei reabilitari din anul 1800.
</div>
