<img src="images/palate\dicastrial TiBi Nicolaevici.jpg" style="width:100%">
<div class="text2"><h2>Palatul Dicasterial </h2>
Clădirea monumentală, cu 4 nivele și trei curți interioare, șase porți și 273 de birouri a fost construită în stilul renașterii italiene, imitând Palatul Medici din Florența. În urma lucrărilor care au durat 5 ani (1855-1860) a rezultat a clădire administrativă sobră, monotonă și lipsită de ornamente. Astăzi adăpostește sediul Judecătoriei, Tribunalului și al Curții de Apel Timișoara.
</div>
