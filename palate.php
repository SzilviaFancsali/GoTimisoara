<div class="text2"><h2>Casa Emmer</h2>
Casa Emmer din centrul orașului Timișoara a fost construită în 1908 după planurile arhitectului László Székely. Are patru nivele și a fost construit în stil eclectic și Art Nouveau. Fațada are trei părți, cele laterale mai înalte cu trei ferestre alugite în partea superioară și partea de mijloc, mai joasă, cu câte patru ferestre pe etaj iar la nivelul ultimului etaj este ampalsată și o statuietă.
