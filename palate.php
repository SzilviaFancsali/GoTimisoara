<div class="text2"><h2>Palatul Dejan</h2>
Palatul Dejan sau Deschan, a fost construit în 1735 de către consilierul Deschan în stil neoclasic. Clădirea are trei nivele, parter și două etaje, iar fațada este împodobiță cu niște coloane corintice. Propriarii acestui palat au fost de origine franceză, dar ulterior și-au schimbat numele în Deschann de Hansen. Drept urmare mulți cunosc palatul sub numele de Deschan. Mai târziu devine locul primului Bazar din Timișoara, și de atunci a fost cunoscut sub acest nume. După revoluție clădirea a fost retrocedată și renovată de actualii proprietari. În prezent în interiorul ei sunt spații comerciale.
</div>
</div>
