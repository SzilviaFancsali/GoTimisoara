<div class="text2"><h2>Palatul Löffler </h2>
Palatul Löffler din Piața Victoriei, a fost construit între anii 1912-1913 de familia si firma antreprenorului Leopold Löffler, ca sediu de firmă și casă familiară pentru el și cei trei fii ai săi. Este o construcție cu 4 nivele, cu spații comerciale la parter. Fațadele de capăt sunt cele mai frumoase. La partea superioară sub niște ferestre ovale, se văd câte două grupuri statuare. Imediat sub ele pe două nivele sunt balcoane mari. Fațada dinspre Piața Victoriei este mult mai simplă, părțile superioare ale părților care ies în afară au doar niște geamuri dreptunghiulare.
