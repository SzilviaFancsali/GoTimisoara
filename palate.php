Palatul Hilt-Vogel se află în Piața Victoriei, și este una din clădirile construită în stil art-nouveau proiectat de renumitul arhitect László Székely. A fost ridicată între 1912 - 1913. Este lipită de o altă clădire Palatul Széchenyi proiectată de același arhitect.
