<div class="text2"><h2>Palatul Lloyd</h2>
Palatul Lloyd este amplasat în Piaţa Victoriei din Timişoara, construit între anii 1910 şi 1912. Festivitatea de deschidere a fost în data de 29 septembrie 1912. Impunătoarea construcţie a fost realizată după planurile arhitectului Leopold Baumhorn, în stil eclectic cu accente din stilul seccesion. 

Palatul Lloyd este o clădire ce face parte din curentul cu numele secession. Stilul secession este o reacție la obiceiurile artistice academice care s-au inspirat din modelele greco-romane ce poate fi considerată și o tentativă de a concilia arta cu descoperirile tehnologice. Acest stil dorește să combine frumosul cu utilul, plăcutul cu funcționalul. Această mișcare artistică, al cărei promotor a fost arhitectul Victor Horta, de naționalitate belgiană, mai poartă denumirea de Art Nouveau, Liberty, Modernista sau Judendstil.</div>
