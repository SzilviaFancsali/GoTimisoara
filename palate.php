<div class="text2"><h2>Palatul Merbl</h2>
În Piața Victoriei se gaseste si Palatul Marbl, fiind construit dupa schitele arhitectului Arnold Merbl. Inaltat in anul 1911, palatul configureaza arhitectura simbol a orasului.
Cladirea este locuita in mare parte de timisoreni, in 24 de apartamente, pe trei etaje, iar la parter sunt patru pravalii. Majoritatea apartamentelor sunt locuinte personale, 16, restul spatii inchiriate.
</div>
