<div class="text2"><h2>Podul Decebal</h2>
Podul Decebal, care face legătura între cartierele Fabric și Cetate, a fost construit în anul 1909 și era, la acea vreme, podul pe grinzi de beton armat cu cea mai mare deschidere din Europa. Are o lungime de 195m și o lățime de 9 metrii. Proiectul a obținut Diploma de onoare la Expoziția Internațională de la Paris din anul 1910. Soluția tehnică de realizare concepută de inginerul Gyözö Mihailich a fost tratată în lucrări de specialitate în mai multe țări. Podul este denumit și podul de pe Aleea Parcului sau Podul Băile Neptun.</div>
