<div class="text2"><h2>Podul Andrei Șaguna</h2>
Podul a fost ridicat în anul 1913 şi a înlocuit vechiul pod de lemn care era construit din 1718 şi ranforsat în 1898, fiind utilizat neîntrerupt. Proiectul inţial prevăzuse amplasarea a patru statui pe balustrade care să reprezinte episcopi româno-catolici. Din cauza izbucnirii războiului, sculptorul Zsigmond Kisfaludi Strobl a plecat cu banii, fără să mai onoreze comanda.
În documentaţia şi hărţile perioadei de la mijlocul secolului XIX este găsit şi sub denumirea de podul „Püspök-híd” sau „Bischofsbrücke”.
Pe vremuri, podul de lemn, şi mai târziu cel din fier, era alăturat podului de cale ferată care venea de la gara de nord spre Baziaş, pe traseul actualului bulevard al Republicii, urmărind linia parcului Scudier şi Catedralei. Acest pod cu cale ferată a fost relocat cu întreg traficul feroviar pe podul Modos, în 1932, renunțându-se la traficul feroviar prin centrul oraşului.</div>
