<div class="text2"><h2>Podul Metalic</h2>
Este, poate cel mai frumos pod de pe Bega. Se cheamă și podul Eiffel, după o legendă comform căreia ar fi fost construit pe baza unui desen al lui Gustave Eiffel, cel care a proiectat și turnul din Paris. În realitate a fost proiectat de Toth Robert, șef secție poduri, la fabrica de șine Reșita. A fost construit pe locul unde funcționa cândva bacul, reutilizând piesele fostului pod metalic Hunyadi. Acesta a fost demontat în 1913, și a fost reconstruit în 1917. A rămas doar un pod de pietoni, fiindcă porțiunile de acces pentru circulație rutieră ar fi necesitat ridicarea rampelor de acces, care ar fi dus la îngroparea caselor din zonă.</div>
