<div class="text2"><h2>Podul de Fier</h2>
În anul 1901 podul de lemn care lega cele două maluri ale râului Timişului – aflat chiar în mijlocul Lugojului – s-a deteriorat. A fost nevoie de construirea altuia, aşa că municipalitatea a comandat la Uzinele din Reşiţa peste 185 de tone de grinzi şi accesorii din fier, aduse la Lugoj cu care trase de câte patru boi. În luna august 1902, podul este inaugurat, uşurându-se astfel legăturile dintre sud-estul Europei şi vestul continentului. Noua construcţie a devenit un simbol al oraşului de pe Timiş.</div>
