<div class="text2"><h2>Podul Michelangelo</h2>
Podul Michelangelo sau Michelangelobrücke este unul dintre podurile tinere, ridicat în 1970 cu ocazia sistematizării şi modernizării oraşului de pe Bega. Proiectul a fost încredinţat inginerului Decebal Anastăsescu şi arhitectului Nicolae Danciu.
Este structurat cu alei pietonale pe ambele părţi şi cu benzi duble în ambele sensuri, fiind una dintre cele mai traficate zone de acces peste Bega. Este cunoscut ca fiind podul cel mai lat din oraş, mult prea apropiat de nivelul apei, care obstrucţionează navigarea ambarcaţiunilor mai înalte spre turbinele de apă. Iniţial, în perioada anilor 1940, trecerea se făcea printr-o pasarelă de lemn, înlocuită cu una nouă în 1964, care făcea legătura spre facultate. </div>
</div>
