<div class="text2"><h2>Podul Mihai Viteazul</h2>
Podul Mihai Viteazul sau "Podul de la Turbina" dar cunoscut și sub denumirea "Podul Morii" a fost construit în 1909. În perioada interbelică, a avut patru turnuri cu basoreliefuri și a fost traversat și de tramvaie. 1981 a fost reconstruit, iar basoreliefurile - o figură feminină ținând stema orașului, un morar cu sacul de făină, un nisipar încărcând o barcă și un tăbăcar - au fost păstrate și montate la capetele podului.</div>
